    private void bt_autenticarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_autenticarActionPerformed
        
        AutenticarControle autenticarC = new AutenticarControle();        
        GUIFX g = new GUIFX();
        g.start();
        
        if(Utilidades.textoVazio(tf_usuario)) g.animar(tf_usuario, GUIFX.EFEITO_TREMER, 40);
        
        else if(Utilidades.senhaVazia(pf_senha)) g.animar(pf_senha, GUIFX.EFEITO_TREMER, 40);
        
        else {
            
            int x = autenticarC.verificarLogin(tf_usuario, pf_senha);
            
            if(x == 0) lb_info.setText("Usuario ou senha incorretos!");
            
            else {
                
                FuncionarioBean funcionarioB = new FuncionarioBean();
                FuncionarioDAO funcionarioD = new FuncionarioDAO();
                
                funcionarioB = funcionarioD.buscar(x);
                
                Mensagem.okMsg("Bem vindo "+funcionarioB.getNome());                                
                this.dispose();
            }
        }
    }//GEN-LAST:event_bt_autenticarActionPerformed

