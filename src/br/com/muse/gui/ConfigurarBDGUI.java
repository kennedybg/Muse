    private void bt_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_salvarActionPerformed
        
        if(confirmarSenha()) {
            
            ArrayList<String> s = new ArrayList<>();
            ConfigurarBDControle configurar = new ConfigurarBDControle();        
            s.add(tf_host.getText());
            s.add(tf_porta.getText());
            s.add(tf_base.getText());
            s.add(tf_usuario.getText());
            s.add(new String(pf_senha.getPassword()));
            configurar.alterar(s);
            
            this.dispose();
            Mensagem.okMsg("As configurações foram salvas!", "Manutenção");            
        }        
    }//GEN-LAST:event_bt_salvarActionPerformed

