    private void bt_resetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_resetActionPerformed

        if(confirmarSenha()) {
            
            ConfigurarBDControle configurar = new ConfigurarBDControle();            
            configurar.redefinir();
            mostrarConfigs();
            Mensagem.okMsg("As configurações foram restauradas com sucesso!", "Manutenção");            
        }
        
    }//GEN-LAST:event_bt_resetActionPerformed

    private void bt_testarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_testarActionPerformed
        
        ConfigurarBDControle configurar = new ConfigurarBDControle();
        
        if(configurar.testarConexao(tf_host.getText(), tf_porta.getText(), tf_base.getText(), tf_usuario.getText(), new String(pf_senha.getPassword()))) Mensagem.okMsg("Conexao bem sucedida!", "Manutenção");
        
        else Mensagem.erroMsg("Não foi possível conectar\nVerifique as configurações!", "Manutenção");
        
    }//GEN-LAST:event_bt_testarActionPerformed

    private boolean confirmarSenha() {
        
        ConfigurarBDControle configurar = new ConfigurarBDControle();
        JPanel panel = new JPanel(new GridLayout(3,1));
        JLabel label1 = new JLabel("Para confirmar as alterações");
        JLabel label2 = new JLabel("Digite sua senha atual:");
        JPasswordField pass = new JPasswordField(22);
        panel.add(label1);
        panel.add(label2);
        panel.add(pass);
        
        String[] options = new String[]{"Confirmar", "Cancelar"};
        
        int option = JOptionPane.showOptionDialog(null, panel, "Manutenção",JOptionPane.NO_OPTION, 2,null, options, pass);

        if (option == 0) if(configurar.autenticar(new String(pass.getPassword()))) return true; else Mensagem.erroMsg("Falha na autenticação!\nDigite a senha atual do administrador do sistema!", "Manutenção");
        
        return false;        
    }

