    private void mostrarConfigsMuse() {
        
        ArrayList<String> cfg = new ArrayList<>();
        MuseConfigControle c = new MuseConfigControle();
        
        cfg = c.carregar();
                
        cb_video.setSelected(Boolean.parseBoolean(cfg.get(0)));
        cb_rede.setSelected(Boolean.parseBoolean(cfg.get(1)));
        
        tf_usuario.setText(cfg.get(2));
        pf_senha.setText("");
        
        for(int x=15;x<481;x+=5) cb_tempo.addItem(x);
        
        cb_tempo.setSelectedItem(Integer.parseInt(cfg.get(4))/60000);
        cb_uf.setSelectedItem(cfg.get(5));
    }

