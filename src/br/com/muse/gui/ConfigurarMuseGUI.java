    private void bt_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_salvarActionPerformed

        ArrayList<String> configs = new ArrayList<>();
        MuseConfigControle c = new MuseConfigControle();
        
        configs.add(String.valueOf(cb_video.isSelected()));
        configs.add(String.valueOf(cb_rede.isSelected()));
        configs.add(tf_usuario.getText());
        configs.add(new String(pf_senha.getPassword()));        
        configs.add(String.valueOf((int) cb_tempo.getSelectedItem() * 60000));
        configs.add(String.valueOf(cb_uf.getSelectedItem()));

        if(confirmarSenha()) {

            c.alterar(configs);            
            this.dispose();            
            Mensagem.okMsg("\nAs configurações foram salvas!\n\nO Muse deve ser reiniciado\npara aplicar as alterações.", "Manutenção");
        }
                               
    }//GEN-LAST:event_bt_salvarActionPerformed

    private boolean confirmarSenha() {
        
        MuseConfigControle c = new MuseConfigControle();
        JPanel panel = new JPanel(new GridLayout(3,1));
        JLabel label1 = new JLabel("Para confirmar as alterações");
        JLabel label2 = new JLabel("Digite sua senha atual:");
        JPasswordField pass = new JPasswordField(22);
        panel.add(label1);
        panel.add(label2);
        panel.add(pass);
        
        String[] options = new String[]{"Confirmar", "Cancelar"};
        
        int option = JOptionPane.showOptionDialog(null, panel, "Manutenção",JOptionPane.NO_OPTION, 2,null, options, pass);

        if (option == 0) if(c.autenticar(new String(pass.getPassword()))) return true; else Mensagem.erroMsg("Falha na autenticação!\nDigite a senha atual do administrador do sistema!", "Manutenção");
        
        return false;        
    }    

